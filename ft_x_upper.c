#include "ft_printf.h"

void	ft_x_upper(unsigned int c, int *i)
{	
	if (c <= 9)
	{
		c = c + 48;
		write(1, &c, 1);
		(*i)++;
	}
	else if (c > 9 && c <= 15)
	{
		c = c + 55;
		write(1, &c, 1);
		(*i)++;
	}
	else
	{
		ft_x_upper((c / 16), i);
		ft_x_upper((c % 16), i);
	}
}
