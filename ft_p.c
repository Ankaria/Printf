#include "ft_printf.h"

void	ft_p(unsigned long long int n, int *i)
{
	if (n <= 9)
	{
		n = n + 48;
		write(1, &n, 1);
		(*i)++;
	}
	else if (n > 9 && n <= 15)
	{
		n = n + 87;
		write(1, &n, 1);
		(*i)++;
	}
	else
	{
		ft_p((n / 16), i);
		ft_p((n % 16), i);
	}
}
